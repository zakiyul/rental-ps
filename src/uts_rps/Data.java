package uts_rps;

public class Data {
    private int id;
    private String nama;
    private String tipe_PS;
    private int durasi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTipe_PS() {
        return tipe_PS;
    }

    public void setTipe_PS(String tipe_PS) {
        this.tipe_PS = tipe_PS;
    }

    public int getDurasi() {
        return durasi;
    }

    public void setDurasi(int durasi) {
        this.durasi = durasi;
    }
    
    
}
